PROMPT 5. Active User Sessions
PROMPT =========================================

COLUMN USERNAME FORMAT A15
COLUMN STATUS FORMAT A10
COLUMN PROGRAM FORMAT A35

SELECT
    SID,
    SERIAL#,
    USERNAME,
    STATUS,
    PROGRAM
FROM
    V$SESSION
WHERE
    STATUS='ACTIVE'
AND USERNAME IS NOT NULL;


